    public void fill_table(){ // write dermacosmetic table
        int i=-1;
        for (Product product : UserStarting.purchaseList) {
                 double cost=calculateDisPrice(product.getPrice());
                 totalCost+=cost;
                 i++;
                 costWithoutDis+=product.getPrice();
                 table_cart.insertRow(table_cart.getRowCount(), new Object[]{
                     
                 product.getBarcode(),product.getProductName(),product.getPrice(),String.valueOf(cost),UserStarting.typeList.get(i)
            }); 
        }
    }
    private double calculateDisPrice(double price){
        HashMap<String,Integer> hm = FileOperations.readInsurance();
        int dis = hm.get(((Customer)Login.currentUser).getInsurance());
        double newprice = (double)(price * (100-dis)/100);
        return newprice; 
    }
    private boolean controlInputs(){ // input controls
        if(tfAddress.getText().isEmpty()){
            JOptionPane.showMessageDialog(Purchase.this, "Please Type Your Address.");
            return false;
        }
        else if(boxCity.getSelectedItem().equals("-")){
            JOptionPane.showMessageDialog(Purchase.this, "Please Select City.");
            return false;
        }
        else if(tfCardNo.getText().length()!=16){
            JOptionPane.showMessageDialog(Purchase.this, "Invalid Card No");
            return false;
        }
        else if(tfCardNo.getText().length()==16){
            for (int i = 0; i < tfCardNo.getText().length(); i++) {
                if(tfCardNo.getText().charAt(i) <48 || tfCardNo.getText().charAt(i)>57){
                    JOptionPane.showMessageDialog(Purchase.this, "Invalid Card No");
                    return false;
                }
            }
        }
        else if(tfNameSurname.getText().isEmpty()){
            JOptionPane.showMessageDialog(Purchase.this, "Please Type Name on Card.");
            return false;
        }
        else if(boxMonth.getSelectedItem().equals("-") || boxYear.getSelectedItem().equals("-")){
            JOptionPane.showMessageDialog(Purchase.this, "Invalid Expire Date.");
            return false;
        }
        else if(tfCVC.getText().length()!=3){
            JOptionPane.showMessageDialog(Purchase.this, "Invalid CVC2 No.");
            return false;
        }
        for (int i = 0; i < tfCVC.getText().length(); i++) {
            if(tfCVC.getText().charAt(i)<48 || tfCVC.getText().charAt(i)>57){
                JOptionPane.showMessageDialog(Purchase.this, "Invalid CVC2 No.");
                return false;
            }
        }
        return true;
    }
