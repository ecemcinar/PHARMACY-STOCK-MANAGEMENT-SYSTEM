    private void buttonCompleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonCompleteMouseClicked
        if(controlInputs()){
            ArrayList<Medicament> medList = FileOperations.readMedicamentList();
            ArrayList<BabyMotherCare> momBabyList = FileOperations.getMotherBabyCareList();
            ArrayList<Dermacosmetic> dermaList = FileOperations.readDermacosmeticList();
            int i=0;
        while(i<tableCart.getRowCount()){
            if(tableCart.getValueAt(i, 4).equals("Medicament")){
                for (int j = 0; j < medList.size(); j++) {
                    if(medList.get(i).getBarcode().equals(tableCart.getValueAt(i,0))){ // get products from list
                        int oldStock = medList.get(i).getStockQ();
                        medList.get(i).setStockQ(oldStock-1); // decrease the stock beacuse it has been purchased
                        FileOperations.updateMedicamentTxt(medList); // update the txt
                        break;
                    }
                }
            }
            else if(tableCart.getValueAt(i, 4).equals("Mother And Baby Care")){
                for (int j = 0; j < momBabyList.size(); j++) {
                    if(momBabyList.get(i).getBarcode().equals(tableCart.getValueAt(i,0))){
                        int oldStock = momBabyList.get(i).getStockQ();
                        momBabyList.get(i).setStockQ(oldStock-1); // decrease the stock beacuse it has been purchased
                        FileOperations.updateMomBabyTxt(momBabyList); // update the txt
                        break;
                    }
                }
            }
            else{ // Dermacosmetic
                for (int j = 0; j < dermaList.size(); j++) {
                    if(dermaList.get(i).getBarcode().equals(tableCart.getValueAt(i,0))){
                        int oldStock = dermaList.get(i).getStockQ();
                        dermaList.get(i).setStockQ(oldStock-1); // decrease the stock beacuse it has been purchased
                        FileOperations.updateDermacosmeticTxt(dermaList); // update the txt
                        
                        break;
                    }
                }
            }
              
            
            i++;
        }
           //close unneccesary panels
          panelBuy.setVisible(false);
          panelCart.setVisible(false);
          panelComp.setVisible(true);
            
        }
        
        
    }//GEN-LAST:event_buttonCompleteMouseClicked

